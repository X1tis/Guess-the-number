using System;  // Подключаем пространство имён для работы с базовыми функциями C#
class Program
{
    static void Main()
    {
        Random random = new Random();  // Создаём экземпляр класса Random для генерации случайных чисел
        int secretNumber = random.Next(1, 101);  // Генерируем случайное число от 1 до 100 включительно
        int guess = 0;  // Переменная для хранения догадки пользователя
        int attempts = 0;  // Переменная для подсчёта попыток
        Console.WriteLine("Добро пожаловать в игру 'Угадай число'!");
        Console.WriteLine("Я загадал число от 1 до 100. Попробуйте угадать его.");
        while (guess != secretNumber)  // Цикл продолжается до тех пор, пока догадка не равна загаданному числу
        {
            Console.Write("Введите ваше предположение: ");
            string input = Console.ReadLine();  // Читаем ввод пользователя как строку
            if (!int.TryParse(input, out guess))  // Пытаемся преобразовать строку в число
            {
                Console.WriteLine("Пожалуйста, введите корректное число.");  // Сообщение об ошибке, если ввод некорректен
                continue;  // Переходим к следующей итерации цикла
            }
            attempts++;  // Увеличиваем количество попыток на 1
            if (guess < secretNumber)  // Если догадка меньше загаданного числа
            {
                Console.WriteLine("Загаданное число больше.");
            }
            else if (guess > secretNumber)  // Если догадка больше загаданного числа
            {
                Console.WriteLine("Загаданное число меньше.");
            }
            else  // Если догадка равна загаданному числу
            {
                Console.WriteLine($"Поздравляю! Вы угадали число {secretNumber} за {attempts} попыток.");
            }
        }
        Console.WriteLine("Спасибо за игру!");
    }
}
